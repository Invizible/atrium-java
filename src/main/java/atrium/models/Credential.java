public class Credential {
    private String field_name;
    private String guid;
    private String label;
    private String type;

    public String getField_Name() {
        return field_name;
    }

    public String getGuid() {
        return guid;
    }

    public String getLabel() {
        return label;
    }

    public String getType() {
        return type;
    }
}
