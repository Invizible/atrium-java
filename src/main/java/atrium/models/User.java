public class User {
    private String guid;
    private String identifier;
    private String is_disabled;
    private String metadata;

    public String getGuid() {
        return guid;
    }

    public String getIdentifier() {
        return identifier;
    }

    public String getIs_disabled() {
        return is_disabled;
    }

    public String getMetadata() {
        return metadata;
    }
}

